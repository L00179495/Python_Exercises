# Program to Determine the various Data Types in python.For ex Integer,Float,String,Boolean etc


#Integer Data Type
a = 5
print("Type of a: ", type(a))

#Float Data Type
b = 5.0
print("\nType of b: ", type(b))

#Complex Data Type
c = 2 + 4j
print("\nType of c: ", type(c))

#None Data Type
d = None
print("\nType of e: ", type(d)) 

# String Data Type
e = 'Infrastructure as Code'
print("\nType of d: ", type(e)) 
  
#Boolean Data Type
f = True
print("\nType of d: ", type(f)) 