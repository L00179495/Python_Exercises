# Determining Types
#Integer
a = 5
print("Type of a: ", type(a))

#Float
b = 5.0
print("\nType of b: ", type(b))

#Complex
c = 2 + 4j
print("\nType of c: ", type(c))

#None
d = None
print("\nType of e: ", type(d)) 

# String 
e = 'Infrastructure as Code'
print("\nType of d: ", type(e)) 
  
#Boolean
f = True
print("\nType of d: ", type(f)) 


#Dynamic Typing
# This will store 6 in the memory and binds the 
# name x to it. After it runs, type of x will 
# be int. 
x = 5 
  
print(type(x)) 
  
# This will store 'hello' at some location int  
# the memory and binds name x to it. After it 
# runs type of x will be str. 
x = 'hello' 
  
print(type(x)) 